-- Database: db_sisajejakuang
-- Buat database jika belum ada
CREATE DATABASE IF NOT EXISTS `db_sisajejakuang` DEFAULT CHARACTER SET utf8mb4 COLLATE utf8mb4_unicode_ci;
USE `db_sisajejakuang`;

-- --------------------------------------------------------

-- Struktur dari tabel `tb_user`
CREATE TABLE IF NOT EXISTS `tb_user` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `name` varchar(255) NOT NULL,
  `email` varchar(255) NOT NULL,
  `password` varchar(255) NOT NULL,
  `role` enum('admin','user') NOT NULL DEFAULT 'user',
  `created_at` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `updated_at` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  UNIQUE KEY `email` (`email`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- Struktur dari tabel `tb_buku_tabungan`
CREATE TABLE IF NOT EXISTS `tb_buku_tabungan` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `user_id` int(11) NOT NULL,
  `nama_buku` varchar(255) NOT NULL,
  `saldo_awal` decimal(15,2) NOT NULL DEFAULT '0.00',
  `saldo_saat_ini` decimal(15,2) NOT NULL DEFAULT '0.00',
  `created_at` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `updated_at` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  KEY `user_id` (`user_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- Struktur dari tabel `tb_anggaran`
CREATE TABLE IF NOT EXISTS `tb_anggaran` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `buku_tabungan_id` int(11) NOT NULL,
  `nama_anggaran` varchar(255) NOT NULL,
  `batas_limit` decimal(15,2) NOT NULL DEFAULT '0.00',
  `created_at` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `updated_at` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  KEY `buku_tabungan_id` (`buku_tabungan_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- Struktur dari tabel `tb_kategori`
CREATE TABLE IF NOT EXISTS `tb_kategori` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `user_id` int(11) DEFAULT NULL, -- NULL jika kategori master global
  `nama_kategori` varchar(255) NOT NULL,
  `jenis_kategori` enum('master','kustom') NOT NULL DEFAULT 'kustom',
  `created_at` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `updated_at` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  KEY `user_id` (`user_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- Struktur dari tabel `tb_transaksi`
CREATE TABLE IF NOT EXISTS `tb_transaksi` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `user_id` int(11) NOT NULL,
  `buku_tabungan_id` int(11) NOT NULL,
  `anggaran_id` int(11) DEFAULT NULL, -- NULL jika anggaran bebas / tanpa anggaran
  `kategori_id` int(11) NOT NULL,
  `tanggal_transaksi` date NOT NULL,
  `keterangan` varchar(255) NOT NULL,
  `nominal` decimal(15,2) NOT NULL DEFAULT '0.00',
  `prioritas` enum('Kebutuhan','Keinginan') NOT NULL DEFAULT 'Kebutuhan',
  `bukti_pengeluaran` varchar(255) DEFAULT NULL,
  `input_method` enum('manual','ocr') NOT NULL DEFAULT 'manual',
  `created_at` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP,
  `updated_at` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`),
  KEY `user_id` (`user_id`),
  KEY `buku_tabungan_id` (`buku_tabungan_id`),
  KEY `anggaran_id` (`anggaran_id`),
  KEY `kategori_id` (`kategori_id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- Struktur dari tabel `tb_system_logs`
CREATE TABLE IF NOT EXISTS `tb_system_logs` (
  `id` int(11) NOT NULL AUTO_INCREMENT,
  `actor` varchar(255) NOT NULL,
  `action` varchar(255) NOT NULL,
  `status` varchar(50) NOT NULL DEFAULT 'success',
  `created_at` datetime NOT NULL DEFAULT CURRENT_TIMESTAMP,
  PRIMARY KEY (`id`)
) ENGINE=InnoDB DEFAULT CHARSET=utf8mb4 COLLATE=utf8mb4_unicode_ci;

-- --------------------------------------------------------

-- Relasi dan Foreign Keys
ALTER TABLE `tb_buku_tabungan`
  ADD CONSTRAINT `fk_buku_tabungan_user` FOREIGN KEY (`user_id`) REFERENCES `tb_user` (`id`) ON DELETE CASCADE;

ALTER TABLE `tb_anggaran`
  ADD CONSTRAINT `fk_anggaran_buku` FOREIGN KEY (`buku_tabungan_id`) REFERENCES `tb_buku_tabungan` (`id`) ON DELETE CASCADE;

ALTER TABLE `tb_kategori`
  ADD CONSTRAINT `fk_kategori_user` FOREIGN KEY (`user_id`) REFERENCES `tb_user` (`id`) ON DELETE CASCADE;

ALTER TABLE `tb_transaksi`
  ADD CONSTRAINT `fk_transaksi_user` FOREIGN KEY (`user_id`) REFERENCES `tb_user` (`id`) ON DELETE CASCADE,
  ADD CONSTRAINT `fk_transaksi_buku` FOREIGN KEY (`buku_tabungan_id`) REFERENCES `tb_buku_tabungan` (`id`) ON DELETE CASCADE,
  ADD CONSTRAINT `fk_transaksi_anggaran` FOREIGN KEY (`anggaran_id`) REFERENCES `tb_anggaran` (`id`) ON DELETE SET NULL,
  ADD CONSTRAINT `fk_transaksi_kategori` FOREIGN KEY (`kategori_id`) REFERENCES `tb_kategori` (`id`) ON DELETE CASCADE;

